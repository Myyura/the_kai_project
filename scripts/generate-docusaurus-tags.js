#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const DOCS_TAGS_PATH = path.join(REPO_ROOT, 'docs/tags.yml');

function kebabCase(value) {
  return String(value || '')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .replace(/[^A-Za-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase();
}

function quoteYaml(value) {
  return JSON.stringify(String(value));
}

function getSubsubjectShortId(subsubjectId, subsubject) {
  const subjectId = subsubject?.subject;
  const prefix = subjectId ? `${subjectId}.` : '';
  return prefix && subsubjectId.startsWith(prefix)
    ? subsubjectId.slice(prefix.length)
    : subsubjectId;
}

function getTopicShortId(topicId, topic) {
  const prefix = topic?.subsubject ? `${topic.subsubject}.` : '';
  return prefix && topicId.startsWith(prefix)
    ? topicId.slice(prefix.length)
    : topicId.split('.').pop();
}

function buildPermalink(kind, id, taxonomy) {
  if (kind === 'school') return `/school/${kebabCase(id)}`;

  if (kind === 'subsubject') {
    const subsubject = taxonomy.subsubjects[id];
    const subjectId = subsubject?.subject || 'General';
    return `/subsubject/${kebabCase(subjectId)}/${kebabCase(getSubsubjectShortId(id, subsubject))}`;
  }

  const topic = taxonomy.topics[id];
  const subsubject = taxonomy.subsubjects[topic?.subsubject] || {};
  const subjectId = subsubject.subject || 'General';
  return [
    '/topic',
    kebabCase(subjectId),
    kebabCase(getSubsubjectShortId(topic.subsubject, subsubject)),
    kebabCase(getTopicShortId(id, topic)),
  ].join('/');
}

function buildDescription(kind, id, taxonomy) {
  if (kind === 'school') {
    const school = taxonomy.schoolTags[id];
    return school?.label && school.label !== id ? `${id} (${school.label})` : id;
  }

  if (kind === 'subsubject') {
    const subsubject = taxonomy.subsubjects[id];
    return `${subsubject?.subject || 'General'} / ${getSubsubjectShortId(id, subsubject)}`;
  }

  const topic = taxonomy.topics[id];
  const subsubject = taxonomy.subsubjects[topic?.subsubject] || {};
  return [
    subsubject.subject || 'General',
    getSubsubjectShortId(topic.subsubject, subsubject),
    getTopicShortId(id, topic),
  ].join(' / ');
}

function pushTag(lines, key, kind, taxonomy) {
  lines.push(`${quoteYaml(key)}:`);
  lines.push(`  label: ${quoteYaml(key)}`);
  lines.push(`  permalink: ${quoteYaml(buildPermalink(kind, key, taxonomy))}`);
  lines.push(`  description: ${quoteYaml(buildDescription(kind, key, taxonomy))}`);
}

function buildTagsYaml(taxonomy) {
  const lines = [
    '# This file is generated by scripts/generate-docusaurus-tags.js.',
    '# Edit the subject files under src/data/tagTaxonomy/ instead.',
  ];

  for (const key of Object.keys(taxonomy.schoolTags || {})) {
    pushTag(lines, key, 'school', taxonomy);
  }
  for (const key of Object.keys(taxonomy.subsubjects || {})) {
    pushTag(lines, key, 'subsubject', taxonomy);
  }
  for (const key of Object.keys(taxonomy.topics || {})) {
    pushTag(lines, key, 'topic', taxonomy);
  }

  return `${lines.join('\n')}\n`;
}

function readTaxonomy() {
  return require('../src/data/tagTaxonomy');
}

function readFileOrEmpty(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf-8');
  } catch (error) {
    if (error?.code === 'ENOENT') return '';
    throw error;
  }
}

function main() {
  const check = process.argv.includes('--check');
  const taxonomy = readTaxonomy();
  const next = buildTagsYaml(taxonomy);

  if (check) {
    const current = readFileOrEmpty(DOCS_TAGS_PATH);
    if (current !== next) {
      console.error('docs/tags.yml is out of date. Run: node scripts/generate-docusaurus-tags.js');
      process.exit(1);
    }
    console.log('Docusaurus tags metadata is up to date.');
    return;
  }

  fs.writeFileSync(DOCS_TAGS_PATH, next);
  console.log(`Generated ${path.relative(REPO_ROOT, DOCS_TAGS_PATH)}`);
}

if (require.main === module) main();

module.exports = {
  buildTagsYaml,
  buildPermalink,
  getSubsubjectShortId,
  getTopicShortId,
  kebabCase,
};
