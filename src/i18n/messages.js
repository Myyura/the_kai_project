export function getUiMessages(namespace, language = DEFAULT_LANGUAGE) {
  const messages = UI_MESSAGES[namespace];
  if (!messages) return {};
  const normalizedLanguage = normalizeLanguage(language);
  return messages[normalizedLanguage] || messages[DEFAULT_LANGUAGE] || {};
}

export function getUiMessage(namespace, key, language = DEFAULT_LANGUAGE) {
  const messages = getUiMessages(namespace, language);
  return messages[key] ?? key;
}
